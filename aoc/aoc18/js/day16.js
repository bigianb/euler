function parseData(rawData)
{
    /*
        Data looks like:

        "Before: [1, 2, 3, 3]",
        "14 0 3 3",
        "After:  [1, 2, 3, 0]",
    */
    let out = [];
    for (let i=0; i<rawData.length-2; ++i){
        const str = rawData[i];
        if (str.startsWith("Before:")){
            let s = '{"before": ' + str.substring(7) + ", ";
            ++i;
            s += '"opcode": [' + rawData[i].split(" ").join(",") + '], ';
            ++i;
            s += '"after": ' + rawData[i].substring(6);
            s += "}";
            
            let obj = JSON.parse(s);
            out.push(obj);
        }
    }

    return out;
}

/*
Addition:

addr (add register) stores into register C the result of adding register A and register B.
addi (add immediate) stores into register C the result of adding register A and value B.

Multiplication:

mulr (multiply register) stores into register C the result of multiplying register A and register B.
muli (multiply immediate) stores into register C the result of multiplying register A and value B.

Bitwise AND:

banr (bitwise AND register) stores into register C the result of the bitwise AND of register A and register B.
bani (bitwise AND immediate) stores into register C the result of the bitwise AND of register A and value B.

Bitwise OR:

borr (bitwise OR register) stores into register C the result of the bitwise OR of register A and register B.
bori (bitwise OR immediate) stores into register C the result of the bitwise OR of register A and value B.

Assignment:

setr (set register) copies the contents of register A into register C. (Input B is ignored.)
seti (set immediate) stores value A into register C. (Input B is ignored.)

Greater-than testing:

gtir (greater-than immediate/register) sets register C to 1 if value A is greater than register B. Otherwise, register C is set to 0.
gtri (greater-than register/immediate) sets register C to 1 if register A is greater than value B. Otherwise, register C is set to 0.
gtrr (greater-than register/register) sets register C to 1 if register A is greater than register B. Otherwise, register C is set to 0.

Equality testing:

eqir (equal immediate/register) sets register C to 1 if value A is equal to register B. Otherwise, register C is set to 0.
eqri (equal register/immediate) sets register C to 1 if register A is equal to value B. Otherwise, register C is set to 0.
eqrr (equal register/register) sets register C to 1 if register A is equal to register B. Otherwise, register C is set to 0.
*/

function processInstr(name, opcode, beforeRegs)
{
    let out = beforeRegs.slice(0);
    let A = opcode[1];
    let B = opcode[2];
    let C = opcode[3];
    //Process an opcode based on the name, ignoring the value in the opcode
    switch (name){
        // addr (add register) stores into register C the result of adding register A and register B.
        case 'addr':
        {
            out[C] = beforeRegs[A] + beforeRegs[B];
        }
        break;
        // addi (add immediate) stores into register C the result of adding register A and value B.
        case 'addi':
        {
            out[C] = beforeRegs[A] + B;
        }
        break;
        //mulr (multiply register) stores into register C the result of multiplying register A and register B.
        case 'mulr':
        {
            out[C] = beforeRegs[A] * beforeRegs[B];
        }
        break;
        //muli (multiply immediate) stores into register C the result of multiplying register A and value B.
        case 'muli':
        {
            out[C] = beforeRegs[A] * B;
        }
        break;
        //banr (bitwise AND register) stores into register C the result of the bitwise AND of register A and register B.
        case 'banr':
        {
            out[C] = (beforeRegs[A] & beforeRegs[B]);
        }
        break;
        //bani (bitwise AND immediate) stores into register C the result of the bitwise AND of register A and value B.
        case 'bani':
        {
            out[C] = (beforeRegs[A] & B);
        }
        break;
        //borr (bitwise OR register) stores into register C the result of the bitwise OR of register A and register B.
        case 'borr':
        {
            out[C] = (beforeRegs[A] | beforeRegs[B]);
        }
        break;
        //bori (bitwise OR immediate) stores into register C the result of the bitwise OR of register A and value B.
        case 'bori':
        {
            out[C] = (beforeRegs[A] | B);
        }
        break;
        //setr (set register) copies the contents of register A into register C. (Input B is ignored.)
        case 'setr':
        {
            out[C] = beforeRegs[A];
        }
        break;
        //seti (set immediate) stores value A into register C. (Input B is ignored.)
        case 'seti':
        {
            out[C] = A;
        }
        break;
        //gtir (greater-than immediate/register) sets register C to 1 if value A is greater than register B. Otherwise, register C is set to 0.
        case 'gtir':
        {
            out[C] = (A > beforeRegs[B]) ? 1 : 0;
        }
        break;
        //gtri (greater-than register/immediate) sets register C to 1 if register A is greater than value B. Otherwise, register C is set to 0.
        case 'gtri':
        {
            out[C] = (beforeRegs[A] > B) ? 1 : 0;
        }
        break;
        //gtrr (greater-than register/register) sets register C to 1 if register A is greater than register B. Otherwise, register C is set to 0.
        case 'gtrr':
        {
            out[C] = (beforeRegs[A] > beforeRegs[B]) ? 1 : 0;
        }
        break;
        //eqir (equal immediate/register) sets register C to 1 if value A is equal to register B. Otherwise, register C is set to 0.
        case 'eqir':
        {
            out[C] = (A == beforeRegs[B]) ? 1 : 0;
        }
        break;
        //eqri (equal register/immediate) sets register C to 1 if register A is equal to value B. Otherwise, register C is set to 0.
        case 'eqri':
        {
            out[C] = (beforeRegs[A] == B) ? 1 : 0;
        }
        break;
        //eqrr (equal register/register) sets register C to 1 if register A is equal to register B. Otherwise, register C is set to 0.
        case 'eqrr':
        {
            out[C] = (beforeRegs[A] == beforeRegs[B]) ? 1 : 0;
        }
        break;
        default:
            console.log("unknown opcode " + name);
            out=null;
    }
    return out;
}

